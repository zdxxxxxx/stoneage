function 字符串切割(数据,切割字符)
	数据 = 数据 .. 切割字符
	local t = {}
	local 目前位置 = 1
	repeat
			local nexti = string.find(数据, 切割字符, 目前位置)
			table.insert(t, string.sub(数据, 目前位置,nexti-string.len(切割字符)))
			目前位置 = nexti + string.len(切割字符)
	until 目前位置 > string.len(数据)
	return t
end
