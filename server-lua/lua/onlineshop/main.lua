function 读取商城数据()
	local 文件号;
	local 数据,数量,临时商城数据;
	for i =1,5 do
		if i == 1 then
			临时商城数据 = 商城数据.最新上架;
		elseif i == 2 then
			临时商城数据 = 商城数据.促销商品;
		elseif i == 3 then
			临时商城数据 = 商城数据.消耗商品;
		elseif i == 4 then
			临时商城数据 = 商城数据.道具商品;
		elseif i == 5 then
			临时商城数据 = 商城数据.宠物商品;
		end
		文件号 = assert(io.open("./data/lua/onlineshop/shop"..i..".txt", "r"))
		数据 = 文件号:read("*a"); -- 读取所有内容
		文件号:close();
		if 数据 ~= "" then
			数量 = 0;
			数据 = 字符串切割(数据,"\n");
			for b = 1,table.getn(数据) do
				if string.sub(数据[b],1,1) ~= "#" and string.sub(数据[b],1,1) ~= "" then
					数量 = 数量 + 1;
					临时商城数据[数量] = 字符串切割(数据[b],"|");
					临时商城数据[数量][1] = tonumber(临时商城数据[数量][1]);
					临时商城数据[数量][2] = tonumber(临时商城数据[数量][2]);
				end
			end
		end
	end
end

